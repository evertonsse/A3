    private void preencheTableUsuarios(List<Usuario> usuarios){

        if (usuarios != null ) {

            DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
            model.setRowCount(0);
            usuarios.forEach(u->{
                Object[] linha = {u.getId(), u.getNome() };
                model.addRow(linha);
            });
        }

    }

